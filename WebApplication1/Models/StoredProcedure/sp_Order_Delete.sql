CREATE OR ALTER PROCEDURE dbo.sp_Order_Delete
(
    @Id UNIQUEIDENTIFIER
)

AS
BEGIN

SET NOCOUNT ON;


BEGIN TRY


BEGIN TRANSACTION;



UPDATE OrderHeader

SET

IsDeleted = 1,

ModifiedDate = GETDATE()

WHERE Id=@Id;




UPDATE OrderDetail

SET

IsDeleted = 1,

ModifiedDate = GETDATE()

WHERE OrderHeaderId=@Id;




IF @@ROWCOUNT = 0

BEGIN

    THROW 50002,'Order not found',1;

END



COMMIT TRANSACTION;


END TRY


BEGIN CATCH


IF @@TRANCOUNT > 0

ROLLBACK TRANSACTION;


THROW;


END CATCH


END
GO